    private void presentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_presentActionPerformed
     if(textArea.getText().length()>0){
         //condition if length of number in textarea >0 , convert num2 from String to double.
        num2=Double.parseDouble(textArea.getText());
     }
     else{
         // else if number1 equal number2.
         num1=num2;
     }
     double res=0;
     if(oper!='\u0000'){
         // condition if operation method not being null the code will excute.
     switch(oper){
         //condition of operations chars that will make the operation and result of any char symbol.
         case '+':
          res=num1+num2;
          oper='\u0000';
          break;
          case '-':
          res=num1-num2;
           oper='\u0000';
          break;
          case '*':
          res=num1*num2;
           oper='\u0000';
          break;
          case '/':
          res=num1/num2;
           oper='\u0000';
          break;          
     }
     textArea.setText(String.valueOf(res));
     //to display result in textarea converting it to string.
     show.setText(show.getText()+ num2 +" = ");
     }
     else{
         textArea.setText(num2 + " ");
     }
    }//GEN-LAST:event_presentActionPerformed
