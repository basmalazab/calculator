    private void opreations(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opreations
        num1=Double.parseDouble(textArea.getText());
        //converting num1 from String to double after calling from text area 
        switch(evt.getActionCommand()){
            //condition get the varaible that written on the button when click on it to set it in show jlabel with num1.
            case "+":
            oper='+';
           show.setText(num1+ " + ");
            break;
              case "-":
            oper='-';
           show.setText(num1+ " - ");
            break;
              case "*":
            oper='*';
           show.setText(num1+ " * ");
            break;
              case "/":
            oper='/';
           show.setText(num1+ " / ");
            break;
            

        }
        textArea.setText(" ");
        //delete anything in textarea to put new numbers or operations.
    }//GEN-LAST:event_opreations
